let crc, i, iTop;
export function crc32(str: string) {
    if (!b_table) {
        init();
    }
    crc = -1;
    for (i = 0, iTop = str.length; i < iTop; i++) {
        crc = (crc >>> 8) ^ b_table[(crc ^ str.charCodeAt(i)) & 0xFF];
    }
    return (crc ^ (-1)) >>> 0;
}

export function crc32IndexOf(array: any[], value: any) {
    return crc32(String(value)) % array.length;
}

export function crc32FromIndex(array: any[], value: any) {
    return array[crc32IndexOf(array, value)];
}

export function friendlyColorFromCrc32(value:any, returnColorCode?:boolean): string {
    let o = crc32FromIndex(friendlyColors, value);
    return returnColorCode ? colorsMap.get(o) : o;
}

export function friendlyColorCodeFromCrc32(value:any): string {
    return friendlyColorFromCrc32(value, true)
}
